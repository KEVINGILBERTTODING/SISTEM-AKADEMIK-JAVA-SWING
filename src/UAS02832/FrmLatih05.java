     void filterangka(KeyEvent a){
        char karakter = a.getKeyChar();
        if(!(((karakter >= 'a') && (karakter <= 'z') || (karakter == KeyEvent.VK_BACK_SPACE)  || (karakter == KeyEvent.VK_SPACE)|| (karakter == KeyEvent.VK_DELETE)))){
            getToolkit().beep();
            a.consume();
            JOptionPane.showMessageDialog(null, "Inputan hanya Huruf saja", "Peringatan",1);
        }
                
    }
     
      
