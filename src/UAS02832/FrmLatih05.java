    private void TxtNIMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TxtNIMActionPerformed
        // TODO add your handling code here:
         String temp = TxtNIM.getText();
        String kode = (temp.substring(0,3));
        
        if (kode.equalsIgnoreCase("A11")) {
            TxtPS.setText("Teknik Infromatika S1");
        } else if (kode.equalsIgnoreCase("A22")){
            TxtPS.setText("Teknik Informatika D3");
        }  else if (kode.equalsIgnoreCase("A14")) {
        TxtPS.setText("Desain Komunikasi Visual S1");
        } else if (kode.equalsIgnoreCase("A12")) {
        TxtPS.setText("Sistem Informasi S1");
        } else if (kode.equalsIgnoreCase("A15")) {
        TxtPS.setText("Ilmu Komunikasi S1");
        } else if (kode.equalsIgnoreCase("A16")) {
        TxtPS.setText("Produksi Film Televisi D4");
        } else if (kode.equalsIgnoreCase("A17")) {
        TxtPS.setText("Animasi D4");
        } else {
            TxtNIM.requestFocus();
            
            JOptionPane.showMessageDialog(null, "Program Studi Tidak ditemukan !","Kesalahan", JOptionPane.ERROR_MESSAGE);
            getToolkit().beep();
            return;
            
        }
        TxtNama.setEnabled(true);
        TxtNama.requestFocus(true);
         
    }//GEN-LAST:event_TxtNIMActionPerformed

